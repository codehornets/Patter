# ---------------------------------------------------------------------------
# truncate
# ---------------------------------------------------------------------------


@pytest.mark.unit
class TestTruncate:
    """Truncation preserves system message + last N."""

    def test_preserves_system_plus_last_n(self) -> None:
        ctx = ChatContext("You are helpful.")
        ctx.add_user("msg1")
        ctx.add_assistant("msg2")
        ctx.add_user("msg3")
        ctx.add_assistant("msg4")

        ctx.truncate(2)

        msgs = ctx.get_messages()
        assert len(msgs) == 3  # system + last 2
        assert msgs[0].role == "system"
        assert msgs[0].content == "You are helpful."
        assert msgs[1].content == "msg3"
        assert msgs[2].content == "msg4"

    def test_keeps_last_n_without_system(self) -> None:
        ctx = ChatContext()
        ctx.add_user("msg1")
        ctx.add_assistant("msg2")
        ctx.add_user("msg3")

        ctx.truncate(2)

        msgs = ctx.get_messages()
        assert len(msgs) == 2
        assert msgs[0].content == "msg2"
        assert msgs[1].content == "msg3"

    def test_noop_when_max_exceeds_count(self) -> None:
        ctx = ChatContext("sys")
        ctx.add_user("hi")
        ctx.truncate(100)
        assert ctx.length == 2

    def test_keeps_only_system_when_max_is_zero(self) -> None:
        ctx = ChatContext("sys")
        ctx.add_user("msg1")
        ctx.add_assistant("msg2")

        ctx.truncate(0)

        msgs = ctx.get_messages()
        assert len(msgs) == 1
        assert msgs[0].role == "system"

    def test_empties_without_system_when_max_is_zero(self) -> None:
        ctx = ChatContext()
        ctx.add_user("msg1")

        ctx.truncate(0)

        assert ctx.length == 0

    def test_negative_max_is_noop(self) -> None:
        ctx = ChatContext()
        ctx.add_user("msg1")
        ctx.truncate(-1)
        assert ctx.length == 1


