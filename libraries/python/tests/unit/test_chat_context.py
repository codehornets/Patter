@pytest.mark.unit
class TestCopy:
    """copy creates an independent deep copy."""

    def test_independent_copy(self) -> None:
        ctx = ChatContext("system")
        ctx.add_user("hello")
        ctx.add_assistant("hi")

        cloned = ctx.copy()

        assert cloned.length == ctx.length
        assert [m.content for m in cloned.get_messages()] == [
            m.content for m in ctx.get_messages()
        ]

        # Mutating clone does not affect original
        cloned.add_user("new message")
        assert cloned.length == 4
        assert ctx.length == 3

    def test_preserves_message_ids(self) -> None:
        ctx = ChatContext()
        msg = ctx.add_user("test")
        cloned = ctx.copy()
        assert cloned.get_messages()[0].id == msg.id


# ---------------------------------------------------------------------------
# Serialization (to_json / from_json)
# ---------------------------------------------------------------------------


@pytest.mark.unit
class TestSerialization:
    """Round-trip serialization."""

    def test_round_trip(self) -> None:
        ctx = ChatContext("system prompt")
        ctx.add_user("hello")
        ctx.add_assistant("world")
        ctx.add_tool_result("data", "call_789")

        json_data = ctx.to_json()
        restored = ChatContext.from_json(json_data)

        assert restored.length == ctx.length

        original_msgs = ctx.get_messages()
        restored_msgs = restored.get_messages()

        for i in range(len(original_msgs)):
            assert restored_msgs[i].id == original_msgs[i].id
            assert restored_msgs[i].role == original_msgs[i].role
            assert restored_msgs[i].content == original_msgs[i].content
            assert restored_msgs[i].timestamp == original_msgs[i].timestamp
            assert restored_msgs[i].tool_call_id == original_msgs[i].tool_call_id

    def test_from_json_empty_messages(self) -> None:
        ctx = ChatContext.from_json({"messages": []})
        assert ctx.length == 0

    def test_from_json_missing_messages_key(self) -> None:
        ctx = ChatContext.from_json({})
        assert ctx.length == 0


