@pytest.mark.unit
class TestToAnthropic:
    """to_anthropic separates system message."""

    def test_separates_system(self) -> None:
        ctx = ChatContext("You are helpful.")
        ctx.add_user("Hello")
        ctx.add_assistant("Hi!")

        result = ctx.to_anthropic()

        assert result["system"] == "You are helpful."
        assert result["messages"] == [
            {"role": "user", "content": "Hello"},
            {"role": "assistant", "content": "Hi!"},
        ]

    def test_none_system_when_absent(self) -> None:
        ctx = ChatContext()
        ctx.add_user("Hello")

        result = ctx.to_anthropic()

        assert result["system"] is None
        assert len(result["messages"]) == 1

    def test_uses_first_system_only(self) -> None:
        ctx = ChatContext("first system")
        ctx.add_user("user msg")
        ctx.add_system("second system")

        result = ctx.to_anthropic()

        assert result["system"] == "first system"
        # Second system message is excluded
        assert len(result["messages"]) == 1
        assert result["messages"][0]["content"] == "user msg"

    def test_tool_messages_included(self) -> None:
        ctx = ChatContext()
        ctx.add_user("call the tool")
        ctx.add_tool_result("tool output", "call_456")

        result = ctx.to_anthropic()

        assert result["messages"][1] == {"role": "tool", "content": "tool output"}


