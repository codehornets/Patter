@pytest.mark.unit
class TestToOpenAI:
    """to_openai converts messages to OpenAI format."""

    def test_basic_conversion(self) -> None:
        ctx = ChatContext("You are helpful.")
        ctx.add_user("Hello")
        ctx.add_assistant("Hi!")

        openai = ctx.to_openai()

        assert openai == [
            {"role": "system", "content": "You are helpful."},
            {"role": "user", "content": "Hello"},
            {"role": "assistant", "content": "Hi!"},
        ]

    def test_includes_tool_call_id(self) -> None:
        ctx = ChatContext()
        ctx.add_tool_result("result", "call_123")

        openai = ctx.to_openai()

        assert openai[0] == {
            "role": "tool",
            "content": "result",
            "tool_call_id": "call_123",
        }

    def test_omits_optional_fields_when_not_set(self) -> None:
        ctx = ChatContext()
        ctx.add_user("hello")

        openai = ctx.to_openai()

        assert openai[0] == {"role": "user", "content": "hello"}
        assert "name" not in openai[0]
        assert "tool_call_id" not in openai[0]


